		<h2>
			Documentation: adding artists
		</h2>
		<div class="documentation__basic edit__hidden">
			<h3>
				Auto-linking musicians
			</h3>
			<div class="text text--outlined">
				<ul class="ul--bulleted">
					<li>
						When a band is added, the database will attempt to automatically link any musicians who were in that band, based on the musician's band history.
					</li>
					<li>
						Auto-linking is based on exact spelling&mdash;so it may miss musicians whose band history spells the band name incorrectly. In those cases, the &ldquo;history&rdquo; field on the <a href="/search/musicians/">musician search page</a> can be helpful.
					</li>
					<li>
						Conversely, musicians may be <em>incorrectly</em> linked to a band if they were in a band of the exact same name. It's recommended to check the band's profile and make sure no musicians have been incorrectly linked to it.
					</li>
				</ul>
			</div>
			
			<h3>
				Names
			</h3>
			<div class="text text--outlined">
				<ul class="ul--bulleted">
					<li>Please include all symbols, etc., in the band's name, unless there is some justification for omitting them.</li>
					<li>All bands are automatically given a &ldquo;friendly name&rdquo; which is used in URLs and as hint text&mdash;this can be manually edited afterward. For example, the automatic friendly name for <a class="artist" href="/artists/cure-blast/">CμЯЁ-βLAST</a> was manually changed to <span class="any__note">cure-blast</span>.</li>
					<li>If a different band with the same name exists, you must add some descriptor to the new band's friendly name, such as year formed or location (e.g. <a class="artist" href="/artists/baron-2005/">Baron</a> <span class="any__note">baron-2005</span> vs <a class="artist" href="/artists/baron-2009/">baron</a> <span class="any__note">baron-2009</span>.<br /><br />When adding the new band, temporarily include the descriptor in the name. After adding, edit the band to remove the descriptor from the name, but leave it in the friendly name, so that the two bands can be differentiated.</li>
				</ul>
			</div>
			
			<h3>
				Which artists should be added
			</h3>
			<div class="text text--outlined">
				<ul class="ul--bulleted">
					<li>
						This is a site for visual kei artists, but there are no stringent rules&mdash;use your best judgement.
					</li>
					<li>
						Non-visual bands are allowed if: they are very active in vk, or appear in vk omnibuses, or if you can otherwise justify the addition.
					</li>
					<li>
						Proto-visual bands (positive punk, gothic, glam) are allowed if connected to a vk band or <em>heavily</em> influential to vk.
					</li>
					<li>
						Please make a note in the &ldquo;artist description&rdquo; field if an artist is non-visual.
					</li>
				</ul>
			</div>
